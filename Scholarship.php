  <h1>EIT General Scholarships</h1>
    <h2>Continuing Success Scholarship</h2>
  <p>
  The continuing success scholarship is designed to help current students who have a proven academic track record continue their path of excellence. Recipients will receive up to $2,500 for one semester (Spring 2019).  The eligibility criteria are as follows:

  <ul>Student must have a declared EIT major</ul>
  <ul>Student must be a U.S. citizen or Permanent Resident</ul>
  <ul>Student must be working toward his/her first bachelor’s degree</ul>
  <ul>Student must be enrolled full-time (12 hours) in order to receive full scholarship. Student must be enrolled in 6 hours to qualify (scholarship will be prorated).</ul>
  <ul>Scholarships will be awarded based on academic merit and financial need.</ul>
    
  </p>
  
    <h2>Summer Scholarship*</h2>
      <p>Applicants must:
          <ul>Be a declared, degree-seeking undergraduate EIT major.</ul>
          <ul>Have minimum 3.00 cumulative grade point average.</ul>
          <ul>Be classified as a US citizen or Permanent Resident.</ul>
          <ul>Be registered for summer classes* by May 1 at UALR for consideration.</ul>
        
        <h6>*The EIT Summer Scholarship will only pay for course(s) taken for degree progression.</h6>
      </p>
  
    <h2>Betty Kay and Charles E. Hathaway Endowed Scholarship*</h2>
      <p>
        Awarded to full-time or part-time (must be enrolled in a minimum of 6 credit hours) students enrolled in the second semester of the Information Technology minor or with a declared major in the College of Engineering and Information Technology. 
        <p>Applicants must:</p>
          <ul>Have achieved junior status in a declared major with a cumulative GPA or 2.8.</ul>
          <ul>Must have demonstrated financial need.</ul>
         <h6>*Denotes scholarships available through more than one department</h6>
      </p>
  
    <h2>Engineering Technology/Jacuzzi Scholarship</h2>
      <p>Awarded to full-time or part-time student majoring in the College of Engineering and  Information Technology. Academic accomplishment and financial need will be strongly considered.</p>
      
    <h2>Society of Women Engineers – Mary L Good Endowed Scholarship</h2>
    <p>Awarded to full-time or part-time female students majoring in the College of Engineering and  Information Technology. Applicants must have achieved a 3.00 cumulative gpa.</p>
    
    <h1>Computer Science Schloarships</h1>
    <h2>Arkansas Academy of Computing Scholarship*</h2>
    <p>Awarded to a full-time or part-time student majoring in computer science or information science. Academic accomplishment and financial need will be strongly considered.</p>
    <h6>*Denotes scholarships available through more than one department.</h6>







